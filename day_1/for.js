// The for loop has three sections:
// 1- initial declaration
// 2- condition
// 3- alteration (usually incrementing or decrementing)

// the order of operations is as follows:
// - run the declration once (when you start the loop)
// - Check the condition
// - if the condition returns true then run the code inside the loop
// - after running the code inside the loop then run the alteration (increment here)
// - check the condition again and repeat
for(let i = 10; i <= 20; i++) {
  console.log(i);
}
